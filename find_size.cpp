#include <iostream>
using namespace std;

int main() 
{    //it will give the size of char 
    cout << "Size of char: " << sizeof(char) << " byte" << endl;
    //it will give the size of int 
    cout << "Size of int: " << sizeof(int) << " bytes" << endl;
    //it will give the size of float 
    cout << "Size of float: " << sizeof(float) << " bytes" << endl;
    //it will give the size of double
    cout << "Size of double: " << sizeof(double) << " bytes" << endl;

    return 0;
}
